    private void btnBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookingActionPerformed
 try {
        String sql = "INSERT INTO datahotel (nama_tamu, no_telpon, tipe_kamar, tgl_checkin, tgl_checkout, lama_menginap) VALUES (?, ?, ?, ?, ?, ?)";
        java.sql.Connection conn = projekuasnadita.Koneksi.configDB();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, txtNama.getText());
        pst.setString(2, txtTelpon.getText());
        pst.setString(3, txtTipe.getText());
        pst.setString(4, txtCheckin.getText());
        pst.setString(5, txtCheckout.getText());
        pst.setString(6, txtLama.getText());
        
        pst.execute();
        JOptionPane.showMessageDialog(null, "Data Tamu Berhasil di Tambah");
        load_tabel();
        txtNama.setText("");
        txtTelpon.setText("");
        txtTipe.setText("");
        txtCheckin.setText("");
        txtCheckout.setText("");
        txtLama.setText("");
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }//GEN-LAST:event_btnBookingActionPerformed
    }
