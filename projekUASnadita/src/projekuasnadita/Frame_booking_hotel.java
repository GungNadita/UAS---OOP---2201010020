private void load_tabel(){
         DefaultTableModel model = new
 DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Tamu");
        model.addColumn("No Telpon");
        model.addColumn("Tanggal Check-in");
        model.addColumn("Tanggal Check-out");
        model.addColumn("No Telpon");
        model.addColumn("Lama Menginap");

 //menampilkan data database kedalam tabel
 try {
 int no=1;
 String sql = "select * from datahotel";
 java.sql.Connection
conn=Koneksi.configDB();
 java.sql.Statement
stm=conn.createStatement();
 java.sql.ResultSet
res=stm.executeQuery(sql);
 while(res.next()){
 model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
 }
 tabeldata.setModel(model);

 }catch (SQLException e){
 }
 }
